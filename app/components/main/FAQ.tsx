export default function FAQ() {
  const [openIndex, setOpenIndex] = useState<number | null>(null);

  const toggleFAQ = (index: number) => {
    setOpenIndex(openIndex === index ? null : index);
  };

  return (
    <div className="w-full  mx-auto space-y-4 grid grid-cols-2 max-md:grid-cols-1 gap-4">
      {faqData.map((faq, index) => (
        <div
          key={index}
          className="rounded-lg w-full overflow-hidden "
        >
          {/* السؤال */}
          <button
            onClick={() => toggleFAQ(index)}
            className="w-full flex justify-between items-center p-4 text-right bg-green-50 hover:bg-green-100 transition-colors"
          >
            <span className="font-semibold text-lg text-gray-800">
              {faq.question}
            </span>
            {openIndex === index ? (
              <Minus className="w-6 h-6 text-gray-600" />
            ) : (
              <Plus className="w-6 h-6 text-gray-600" />
            )}
          </button>

          {/* الجواب */}
          {openIndex === index && (
            <div className="p-4 bg-green-50 text-gray-700 leading-relaxed border-t border-gray-200">
              {faq.answer}
            </div>
          )}
        </div>
      ))}
    </div>
  );
}
